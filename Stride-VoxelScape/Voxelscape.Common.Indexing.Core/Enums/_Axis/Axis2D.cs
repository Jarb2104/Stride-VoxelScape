namespace Voxelscape.Common.Indexing.Core.Enums
{
	/// <summary>
	/// An enumeration of the orthogonal axes in two dimensional space used to specify a single axis at a time.
	/// </summary>
	/// <remarks>
	/// The backing values of this enumeration have been selected to match the integer dimension values of a multidimensional array.
	/// </remarks>
	public enum Axis2D
	{
		/// <summary>
		/// The x axis.
		/// </summary>
		X = 0,

		/// <summary>
		/// The y axis.
		/// </summary>
		Y = 1,
	}
}
