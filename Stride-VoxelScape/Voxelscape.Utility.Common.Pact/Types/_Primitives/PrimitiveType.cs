namespace Voxelscape.Utility.Common.Pact.Types
{
	/// <summary>
	/// An enumeration of the primitive types in C#.
	/// </summary>
	public enum PrimitiveType
	{
		/// <summary>
		/// Represents a <see cref="byte"/>.
		/// </summary>
		Byte,

		/// <summary>
		/// Represents a <see cref="sbyte"/>.
		/// </summary>
		SByte,

		/// <summary>
		/// Represents a <see cref="short"/>.
		/// </summary>
		Short,

		/// <summary>
		/// Represents a <see cref="ushort"/>.
		/// </summary>
		UShort,

		/// <summary>
		/// Represents a <see cref="int"/>.
		/// </summary>
		Int,

		/// <summary>
		/// Represents a <see cref="uint"/>.
		/// </summary>
		UInt,

		/// <summary>
		/// Represents a <see cref="long"/>.
		/// </summary>
		Long,

		/// <summary>
		/// Represents a <see cref="ulong"/>.
		/// </summary>
		ULong,

		/// <summary>
		/// Represents a <see cref="float"/>.
		/// </summary>
		Float,

		/// <summary>
		/// Represents a <see cref="double"/>.
		/// </summary>
		Double,

		/// <summary>
		/// Represents a <see cref="decimal"/>.
		/// </summary>
		Decimal,

		/// <summary>
		/// Represents a <see cref="char"/>.
		/// </summary>
		Char,

		/// <summary>
		/// Represents a <see cref="bool"/>.
		/// </summary>
		Bool,
	}
}
